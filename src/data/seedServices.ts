import type { Service } from '../types'

// Seed data ported from the user's Notion price list CSV.
// purchasePrice = "Kosten (Netto)" — what it costs the company per unit
// salePrice     = "Rechnung (Netto)" — what the company charges per unit
// Categories were inferred from the service names.
//
// IMPORTANT: A few rows in the source CSV had outlier sale prices that look
// like data-entry mistakes (e.g. Briefpapier listed at 8.423,36 € for a single
// package). They are imported as-is so nothing is silently changed; review and
// fix them on the "Preisliste" page after first launch.

const now = Date.now()

function svc(
  id: string,
  name: string,
  category: string,
  purchasePrice: number,
  salePrice: number,
  defaultQuantity = 1,
  url?: string,
  note?: string,
  visible = true,
): Service {
  return {
    id,
    name,
    category,
    purchasePrice,
    salePrice,
    defaultQuantity,
    url,
    note,
    visible,
    createdAt: now,
    updatedAt: now,
  }
}

